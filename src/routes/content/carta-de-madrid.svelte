<h2 id="modelo-social">
  El modelo de sociedad que queremos, para una genuina democracia social, económica y política.
</h2>
<p>
  La culpa del desempleo no la tienen los desempleados. El relato de la expiación es un artificio de
  la austeridad para debilitar tu justa aspiración de ser rescatado.
</p>
<p>
  El desempleo, la pérdida de derechos y el desmantelamiento del estado social son el resultado de
  las políticas de rigor que los asesinos sociales hacen en los parlamentos. Con un mercado libre,
  ellos son más fuertes y tú más débil.
</p>
<p>
  En Europa combaten dos visiones de la sociedad y el futuro. Nosotros estamos del lado del pleno
  empleo, la intervención estatal y las políticas económicas orientadas al interés público y la
  felicidad social.
</p>
<p>
  En Madrid activistas de MMT españoles e italianos delimitaron el frente de batalla colocándose en
  defensa del progreso y de la colectividad. Así nace la Carta de Madrid. Si está de acuerdo con su
  contenido, <span style="font-size: 18pt;"
    ><a href="#formulario"><span style="color: #4661ab;"> <strong>¡fírmala!</strong></span></a
    ></span
  >
</p>
